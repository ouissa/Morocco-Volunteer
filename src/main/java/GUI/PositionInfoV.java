    public PositionInfoV(Object frm, int positionId){
        initComponents();
        jTextField1.setEditable(false);
        jTextField2.setEditable(false);
        jTextField3.setEditable(false);
        jTextField4.setEditable(false);
        jButton2.setVisible(false);
        jButton3.setVisible(false);
        jButton4.setVisible(false);
        jButton5.setVisible(false);
        
        this.previousForm = frm;
        if(this.previousForm instanceof SearchPositions)
            jButton3.setVisible(true);
        else if(this.previousForm instanceof ManagePositions)
            jButton2.setVisible(true);
        else if(this.previousForm instanceof ManageRequests){
            jButton4.setVisible(true);
            jButton5.setVisible(true);
        }
        
        try{
            JSONParser parser = new JSONParser();
            String pathToHome= System.getProperty("user.home");
            Object obj = parser.parse(new FileReader(pathToHome + "/NetBeansProjects/VolunteerMorocco/src/main/java/environment_variables/db_credentials.json"));
            JSONObject db_credentials = (JSONObject)obj;

            url = (String) db_credentials.get("url");
            uid = (String) db_credentials.get("username");
            pw = (String) db_credentials.get("password");
            
        } catch (IOException e) {
            System.out.println(e);
        } catch (org.json.simple.parser.ParseException e) {
            System.out.println(e);
        }
        this.positionId = positionId;
        try (Connection conn = DriverManager.getConnection(url, uid, pw); Statement stmt = conn.createStatement()) {
            String qry = "SELECT O.organizationId, O.organizationName, E.name AS \"Event Name\", E.description AS \"Event Description\", P.role" +
                        " FROM Position AS P INNER JOIN Event AS E ON E.eventId = P.positionId" +
                        " INNER JOIN Organization AS O ON O.organizationId = E.organizationId" +
                        " WHERE P.PositionId = "+this.positionId+"";
        
            // Result set get the result of the SQL query
            ResultSet rs = stmt.executeQuery(qry);
            if(rs.next()){
                this.organizationId = rs.getInt(1);
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(5));
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Error");
                return;
            }

            
        }
        catch (SQLException ex){
            System.err.println("SQLException: " + ex);
        }
        
    }

