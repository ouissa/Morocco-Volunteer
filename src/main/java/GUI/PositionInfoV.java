    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try (Connection conn = DriverManager.getConnection(url, uid, pw)) {

            String qry = "INSERT INTO Application"
            + " (volunteerId, positionId, applicationStatus)"
           + " VALUES (?, ?, 'pending');";
            
            
            PreparedStatement prepStmt = conn.prepareStatement(qry);
            prepStmt.setInt (1, currentUserId);
            prepStmt.setInt (2, positionId);
            
            prepStmt.execute();
            JOptionPane.showMessageDialog(this, "The Application has been sent to the Organization");
            
            SearchPositions frm = new SearchPositions();
            frm.setLocation(getLocation());
            frm.setSize(getSize());
            setVisible(false);
            frm.setVisible(true);
            dispose();
            }
            catch (SQLException ex) {
                System.err.println("SQLException: " + ex);
            }
            catch (Exception e) {
                System.err.println("Exception: " + e);
            }
    }//GEN-LAST:event_jButton3ActionPerformed

